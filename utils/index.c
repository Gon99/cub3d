#include "../includes/cub3d.h"

int	iter_spaces_idx(char *line, int i)
{
	while (line[i] == ' ' || line[i] == '\n' || line[i] == '\t' \
			|| line[i] == '\v' || line[i] == '\f' || line[i] == '\r')
		i++;
	return (i);
}
